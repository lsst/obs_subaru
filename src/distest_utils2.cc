void F_CS_CCD2SKY_XY(int ELOrder, double EL,double ***Coef, double X, double Y, double *X_out, double *Y_out)
{
    int i, ix, iy,  ELi;
    double ZCoef[13]={0},Z,**ELZ,XEL,*PX,*PY;
    double *a = NULL;
//    char fCoef[100];

    // CCD2SKY-X CS=0;
    // CCD2SKY-Y CS=1;
    // SKY2CCD-X CS=2;
    // SKY2CCD-Y CS=3;

    PX = (double *) malloc(sizeof(double)*(XYOrder+1));
    PY =  (double *) malloc(sizeof(double)*(XYOrder+1));
    ELZ =  (double **) malloc(sizeof(double *)*12);
    for(i=0;i<12;i++){
        ELZ[i] =  (double *) malloc(sizeof(double)*2);
        ELZ[i][0]=ELZ[i][1]=0;
        ZCoef[i]=0;
    }

    PX[0]=1.0;
    PY[0]=1.0;
    for(i=0;i<XYOrder;i++){
        PX[i+1]=PX[i]*X;
        PY[i+1]=PY[i]*Y;
    }

    /* for X_out = f(X,Y) */
    for(i=0, ELi=30; ELi<90; i++, ELi+=5){
        Z = 0.0;
        //ixy = 0;
        a = Coef[0][i]; // for CCD2SKY X
        for(ix=0; ix<XYOrder+1; ix++) {
            for(iy=0; iy<XYOrder+1-ix; iy++) {
                Z+=(*a)*PX[ix]*PY[iy];
                a++;
            }
        }
        ELZ[i][0]=ELi;
        ELZ[i][1]=Z;
    }
    F_LS1(12,ELOrder,ELZ,ZCoef);
//    for(i=0;i<6;i++)
    XEL = ZCoef[ELOrder-1] + ZCoef[ELOrder] * EL;
    for(i=ELOrder-2; i>=0; i--)
        XEL = ZCoef[i] + EL * XEL;
//        XEL +=ZCoef[i]*pow(EL,i);
    *X_out = XEL;

    /* for Y_out = f(X,Y) */
    for(i=0, ELi=30; ELi<90; i++, ELi+=5){
        Z=0.0;
        //ixy = 0;
        a = Coef[1][i]; // for CCD2SKY Y
        for(ix=0;ix<XYOrder+1;ix++) {
            for(iy=0;iy<XYOrder+1-ix;iy++) {
                Z+=(*a)*PX[ix]*PY[iy];
                a++;
            }
        }
        ELZ[i][0]=ELi;
        ELZ[i][1]=Z;
    }
    F_LS1(12,ELOrder,ELZ,ZCoef);
    XEL = ZCoef[ELOrder-1] + ZCoef[ELOrder] * EL;
    for(i=ELOrder-2; i>=0; i--)
        XEL = ZCoef[i] + EL * XEL;
    *Y_out = XEL;

    for(i=0;i<12;i++) {
        free(ELZ[i]);
    }
    free(ELZ);
    free(PX);
    free(PY);

    return;
}



const double TOLERANCE = 0.005;
// interface function for getting "Distorted pos (pix)" from input:"Undistorted pos (pix)"
// obtain the inversed function by an iterative substitution, rather than using the fitted result
void convUndist2DistPosIterative(float x_undist, float y_undist, float *x_dist, float *y_dist, float elevation, double ***Coef)
{
    int el_interpol_order = 5;

#if 0
    fprintf(stderr, "ok - getDistIterative\n");
    fprintf(stderr, "Coef[0][0][0]: %lg\n", Coef[0][0][0]);
    fprintf(stderr, "Coef[1][0][0]: %lg\n", Coef[1][0][0]);
#endif

    if( elevation<30 || elevation>85 ) {
        fprintf(stderr, "Can't fit EL=%f\n", elevation);
        fprintf(stderr, "We must choose EL from a value between 30 and 85 \n");
    }

    double x_sky_tmp = x_undist;
    double y_sky_tmp = y_undist;

    double x_dist_tmp, y_dist_tmp;
    F_CS_SKY2CCD_XY(el_interpol_order, elevation, Coef, x_sky_tmp, y_sky_tmp, &x_dist_tmp, &y_dist_tmp);

//    fprintf(stderr, "debug: distpos_iter: EL: %f SKY(%f,%f) -> CCD(%f,%f)\n", 
//            elevation, x_sky_tmp, y_sky_tmp, x_dist_tmp, y_dist_tmp);

    int nIterMax = 10;                  // max number of iterations.  Should be configurable
    for(int N=0; N != nIterMax; ++N) {
        F_CS_CCD2SKY_XY(el_interpol_order, elevation, Coef, x_dist_tmp, y_dist_tmp, &x_sky_tmp, &y_sky_tmp);


        double diff_x = x_undist - x_sky_tmp;
        double diff_y = y_undist - y_sky_tmp;

#if 0
        fprintf(stderr, "debug: distpos_iter: %d\n", N);
        fprintf(stderr, "debug: distpos_iter: EL: %f CCD(%f,%f) -> SKY(%f,%f)\n", 
                elevation, x_dist_tmp, y_dist_tmp, x_sky_tmp, y_sky_tmp);
        fprintf(stderr, "debug: distpos_iter: %f  %f\n", diff_x, diff_y);
#endif

        if( fabs(diff_x) < TOLERANCE && fabs(diff_y) < TOLERANCE ) {
            *x_dist = x_dist_tmp;
            *y_dist = y_dist_tmp;

#if 0
            fprintf(stderr, "EL : %f ", elevation);
            fprintf(stderr, " , SKY ( %f", x_undist);
            fprintf(stderr, " , %f", y_undist);
            fprintf(stderr, " ) -> CCD ( %f", *x_dist);
            fprintf(stderr, " , %f\n", *y_dist);
#endif
            return;
        }

        x_dist_tmp += diff_x;
        y_dist_tmp += diff_y; 

    }

    throw LSST_EXCEPT(lsst::pex::exceptions::OutOfRangeException,
                      str(boost::format("Too many iterations undistorting (%.3f, %.3f); "
                                        "giving up after %d at (%.3f, %.3f)")
                          % x_undist % y_undist % nIterMax % x_dist_tmp % y_dist_tmp));
}


